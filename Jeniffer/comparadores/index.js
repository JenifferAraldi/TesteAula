//IDENTICO (===)

/* "1" === "2"  false, são difererntes 
   "2" === "2"  true, são identicos 
    2  === "2"  false, tipo diferentes */
    
    const condicao1 = 1 === 2 
    console.log("IDENTICO")
    console.log(condicao1)

/* o valor que sai da comparação pode ser guardado em uma variavel nesse caso:
 condicao1 === false */


 //IGUAL (==)

/* "1" == "2"  false, são difererntes 
   "2" == "2"  true, são identicos 
    2  == "2"  true, valores iguais */
    
    const condicao2 = 1 == 2 
    console.log("IGUAL")
    console.log(condicao2)

/* o valor que sai da comparação pode ser guardado em uma variavel nesse caso:
 condicao2 == false */


//DIFERENTE (!==)

/* "1" !== "2"  true, são difererntes 
   "2" !== "2"  false, são identicos 
    2  !== "2"  true, tipo diferentes */
    
    const condicao3 = 1 !== 2 
    console.log("DIFERENTE")
    console.log(condicao3)

/* o valor que sai da comparação pode ser guardado em uma variavel nesse caso:
 condicao3 !== false */

 
// MAIOR / MAIOR IGUAL (>/>=)

/* 1 > 2  false, porque 1 é menor que 2 
   2 > 2  false, porque 2 é igual a 2  
   3  > 2  true, porque 3 é maior que 2 */

    const condicao4 = 1 > 2 
    console.log("MAIOR")
    console.log(condicao4)

/* 1 >= 2  false, porque 1 é menor que 2 
   2 >= 2  true, porque 2 é igual a 2  
   3 >= 2  true, porque 3 é maior que 2 */

    const condicao5 = 1 >= 1 
    console.log("MAIOR IGUAL")
    console.log(condicao5)

